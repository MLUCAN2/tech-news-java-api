package com.technews;

public class VariableTypes {
    public static void main(String[] args){
        //For string values, we use string
        String name= "Cleopatra";
        //To use whole numbers, we use int
        int age= 2016+69;
        //To store decimal values, we use double data type
        double price= 1999.99;
        //For boolean values, we use boolean
        boolean truth= true;
        boolean falseness= !truth;
    }
}
